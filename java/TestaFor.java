
public class TestaFor 
     {
	  public static void main(String[] args)  
	       {  
		    int contador;
		    int total=0;
		    
		    for( contador=0; contador<=10; contador+=1 )
		    { 
		    	total=contador+total;
		      System.out.println(total);
		    	  
		    }
		     
		    System.out.println(total);
		  
		    
		
	       }

     }
